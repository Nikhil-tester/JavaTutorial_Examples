package arrays;
//Java Program to print the array elements using for-each loop  

public class Ex3 {

	public static void main(String[] args) {
		
		int arr[]= {11,22,33,44,1000,10,11,77,99,34,89,78};
		
		//for each
		for(int i:arr)
			
			//printing array using for-each loop 
			System.out.println(i);
	
	}

}
